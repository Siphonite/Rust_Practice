// 11. Check if a hardcoded number is zero, positive, or negative using if-else.

fn main() {
    let a = -45;
    
    if a>0 {
     println!("{} is greater than 0",a);
     }
     else if a==0 {
         println!("{} is equal to 0",a);
         }
     else {
         println!("{} is less than 0",a);
     }

}