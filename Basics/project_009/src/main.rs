// 9. Compute the area of a rectangle given its hardcoded length and width.
fn main() {
    let length:f32 = 71.26;
    let breadth:f32 = 45.12;

    println!("The lenght of rectangle is : {} meters", length);
    println!("The breadth of rectangle is : {} meters", breadth);

    let area:f32 = length * breadth;
    println!("\nThe area of rectangle is : {} sq meters", area);
}